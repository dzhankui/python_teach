'''
Вася разобрал на даче старый сарай на n досок одинаковой ширины и толщины, но разной длины. 
Он решил построить из них забор. Высота забора не имеет для Васи особого значения, 
главное чтобы он был как можно более ровным. 
Неровность забора определяется как разница высот между самой короткой и самой длинной доской, использованной для постройки забора. 
После тщательного измерения оказалось, что k из n досок лишние и их можно не использовать для строительства забора.
Определите минимальную неровность забора, который можно построить из n−k досок.
Формат ввода
В первой строке входных данных записаны два числа 
n и k (0≤k<n≤200000) — количество досок от разбора сарая и количество лишних досок.
Во второй строке записаны n целых чисел li(1≤li≤10**9) — длины досок.
Формат вывода
Выведите единственное целое число — минимальную неровность забора.
Input:
2 0
15 19
Output:
4
Сначала сортируем массив длин досок по возрастанию.
Затем выбираем из начала массива (самые короткие доски) k досок и отбрасываем их, так как они лишние.
После этого минимальная неровность забора будет равна разнице между длиной последней доски 
(самой длинной из оставшихся) и длиной первой доски (самой короткой из оставшихся).
'''
n, k = map(int, input().split())
lengths = list(map(int, input().split()))
# Сортируем массив длин досок
lengths.sort()
# Отбрасываем лишние доски
lengths = lengths[k:]
# Вычисляем минимальную неровность забора
minimum_unevenness = lengths[-1] - lengths[0]
print(minimum_unevenness)
